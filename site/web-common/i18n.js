  function format(template, values = {}) {
    return template.replace(/\{([A-Za-z0-9_]+)\}/g, (match, key) => (
      Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : match
    ));
  }

  function text(language, key, values = {}) {
    const table = messages[language] || messages.ja;
    const template = table[key] ?? messages.ja[key] ?? key;
    return format(template, values);
  }

  function translateDocument(language) {
    document.documentElement.lang = language;
    document.querySelectorAll("[data-i18n]").forEach((element) => {
      element.textContent = text(language, element.dataset.i18n);
    });
    ["placeholder", "aria-label", "title"].forEach((attribute) => {
      const dataName = `i18n${attribute.split("-").map((part) => part.charAt(0).toUpperCase() + part.slice(1)).join("")}`;
      document.querySelectorAll(`[data-i18n-${attribute}]`).forEach((element) => {
        element.setAttribute(attribute, text(language, element.dataset[dataName]));
      });
    });
  }

  window.DIGIT_SPAN_I18N = Object.freeze({ messages, text, translateDocument });
